<script>
import { mapGetters } from "vuex";
import overlayComponent from '../../overlayComponent.vue'
export default {
  data() {
    return {
      rules: {
        required: (value) => !!value || "Este campo es requerido.",
      },
      snack: false,
      edit_caliente_partida: false,
      tipo_de_servicio: [
        {
          name: "normal",
          value: 1,
        },
        {
          name: "urgente",
          value: 2,
        },
        {
          name: "programado",
          value: 3,
        },
      ],
      estado_de_la_cotizacion: [
        {
          name: "pendiente",
          value: 1,
        },
        {
          name: "Orden de servicio",
          value: 2,
        },
        {
          name: "Rechazada",
          value: 3,
        },
      ],
      partida: {
        identificacion: "",
        instrumento: {},
        cantidad: 0,
        marca: "",
        modelo: "",
        serie: "",
        importe: 0,
        servicio: {},
        unidad: {},
        unidad_cod: {},
        clave_sat: {},
      },
      servicio_partida: [
        {
          name: "Calibracion",
          value: 1,
        },
        {
          name: "Reparacion",
          value: 2,
        },
        {
          name: "Garantia",
          value: 3,
        },
        {
          name: "Venta",
          value: 4,
        },
      ],
      unidad_partida: [
        {
          name: "servicio",
          Value: 1,
        },
        {
          name: "Pieza",
          Value: 2,
        },
        {
          name: "Juego",
          Value: 3,
        },
      ],
    };
  },
  components:{
  "overlay" : overlayComponent
  },
  computed: {
    ...mapGetters([
      "services",
      "dialog_duplicate_cotizacion",
      "clientes",
      "monedas",
      "empleados",
      "tiempos_de_entrega",
      "instrumentos",
      "cotizacion_para_duplicar",
      "unidades",
      "clavesSat",
    ]),
    openDialog: {
      get() {
        return this.dialog_duplicate_cotizacion;
      },
      set(val) {
        this.$store.commit("setDialogDuplicateCotizacion", val);
      },
    },
    var_computed_importe_instrumento: {
      get() {
        return this.partida.importe;
      },
      set(newVal) {
        this.partida.importe = newVal;
      },
    },
    var_computed_sub_total: {
      get() {
        var result = 0;
        for (var i = 0; i < this.cotizacion_para_duplicar.has_partidas.length; i++) {
          result += parseInt(this.cotizacion_para_duplicar.has_partidas[i].importe);
        }
        this.cotizacion_para_duplicar.sub_total = parseInt(result);
        return this.cotizacion_para_duplicar.sub_total;
      },
      set(newVal) {
        this.cotizacion_para_duplicar.sub_total = newVal;
      },
    },
    var_computed_iva: {
      get() {
        var result = 0,
          item_iva = 0;

        this.cotizacion_para_duplicar.has_partidas.forEach((item) => {
          item_iva = (item.importe * this.cotizacion_para_duplicar.has_cliente.iva) / 100;
          result += item_iva;
        });
        this.cotizacion_para_duplicar.iva = parseInt(result);
        return this.cotizacion_para_duplicar.iva;
      },
      set(newVal) {
        this.cotizacion_para_duplicar.iva = newVal;
      },
    },
    var_computed_total: {
      get() {
        var result = 0;
        result = this.var_computed_sub_total + this.var_computed_iva;
        this.cotizacion_para_duplicar.total = parseInt(result);
        return this.cotizacion_para_duplicar.total;
      },
      set(newVal) {
        this.cotizacion_para_duplicar.total;
      },
    },
  },
  async mounted() {

    this.$store.commit('setOverley', true)
    Promise.all([
      this.services.clienteServices.getlistclientes(),
      this.services.monedaServices.getlistMonedas(),
      this.services.tiempoDeEntregaServices.getlistTiempoDeEntrega(),
      this.services.empleadoServices.getlistEmpleados(),
      this.services.instrumentoServices.getlistInstrumentos(),
      this.services.unidadServices.getUnidades(),
      this.services.claveSatServices.getclavesSat()])
      .then(  () => {
        this.$store.commit('setOverley', false)
      })
      .catch((reason) => {
         this.$store.commit('setOverley', false)
      });
    
  },
  methods: {
    AgregarPartida() {
      for (var i = 0; i < this.partida.cantidad; i++) {
        var obj = {
          identificacion: this.partida.identificacion,
          has_intrumento: this.partida.instrumento,
          cantidad: this.partida.cantidad,
          marca: this.partida.marca,
          modelo: this.partida.modelo,
          serie: this.partida.serie,
          importe: this.partida.importe,
          marca: this.partida.marca,
          modelo: this.partida.modelo,
          serie: this.partida.serie,
          servicio: this.partida.servicio.name,
          unidad: this.partida.unidad.name,
          importe: this.partida.instrumento.precio_venta * 1,
          unidad_cod: this.partida.unidad_cod,
          clave_sat: this.partida.clave_sat,
        };
        this.cotizacion_para_duplicar.has_partidas.push(obj);
      }
      this.partida = {
        identificacion: "",
        instrumento: {},
        cantidad: 0,
        marca: "",
        modelo: "",
        serie: "",
        importe: 0,
        servicio: {},
        unidad: {},
        unidad_cod: {},
        clave_sat: {},
      };
    },
    async AddCotizacion() {
      if (this.$refs.f_mag.validate()) {
        await this.services.cotizacionServices.agregarCotizacionDuplicada(
          this.cotizacion_para_duplicar
        );
        this.cotizacion_para_duplicar = {};
      }
    },
    eliminarPartida(item) {
      this.services.partidaServices.EliminarPartida(item);
      var index = this.cotizacion_para_duplicar.has_partidas.indexOf(item);
      this.cotizacion_para_duplicar.has_partidas.splice(index, 1);
    },
    ActualizarImporte(item) {
      item.importe = item.has_intrumento.precio_venta;
    },
  },
};
</script>
